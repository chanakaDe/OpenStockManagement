    public String generateID() throws ClassNotFoundException, SQLException {
        String id = SupplierOrderController.getLastID();
        if (id == null) {
            return "SOD0001";
        } else {
            int num = Integer.parseInt(id.toString().substring(3));
            num++;
            if (num < 10) {
                return "SOD000" + num;
            } else if (num < 100) {
                return "SOD00" + num;
            } else if (num < 1000) {
                return "SOD0" + num;
            } else {
                return "SOD" + num;
            }
        }
    }

