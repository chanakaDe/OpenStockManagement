    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = idText.getText();
        String supplierID = ((Supplier) supplierCombo.getSelectedItem()).getSupplierID();
        String date = new SimpleDateFormat("yyyy/MM/dd").format(Calendar.getInstance().getTime());
        SupplierOrder supplierOrder = new SupplierOrder(id, supplierID, date);

        int rowCount = orderTable.getRowCount();

        SupplierOrderDetail[] supplierOrderDetails = new SupplierOrderDetail[rowCount];

        for (int i = 0; i < rowCount; i++) {
            String bottleid = (String) orderTable.getValueAt(i, 0);
            String bottleVolume = (String) orderTable.getValueAt(i, 1);
            String bottleQty = (String) orderTable.getValueAt(i, 2);

            SupplierOrderDetail supplierOrderDetail = new SupplierOrderDetail(id, bottleid, bottleVolume, bottleQty);
            supplierOrderDetails[i] = supplierOrderDetail;
        }
        try {
            int res = SupplierOrderController.addSupplierOrder(supplierOrder, supplierOrderDetails);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Added " + id);
            } else {
                JOptionPane.showMessageDialog(this, "Error adding " + id);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddSupplierOrder.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            SupplierOrder[] supplierOrders = SupplierOrderController.getAllOrdersAll();
            DefaultTableModel dtm = (DefaultTableModel) allordersTable.getModel();
            dtm.setRowCount(0);
            for (SupplierOrder supplierOrder1 : supplierOrders) {
                Object[] rows = {supplierOrder1.getSupplierOrderID(), supplierOrder1.getSupplierID(), supplierOrder1.getDate(), supplierOrder1.getStatus()};
                dtm.addRow(rows);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddSupplierOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel dtm = (DefaultTableModel) orderTable.getModel();
        dtm.setRowCount(0);

        try {
            idText.setText(generateID());
        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println("Generate Error by Me");
        }
        orderText.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

