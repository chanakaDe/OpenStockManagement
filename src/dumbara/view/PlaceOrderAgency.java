    public String generateID() throws ClassNotFoundException, SQLException {
        String id = AgencyOrderController.getLastID();
        if (id == null) {
            return "AOID001";
        } else {
            int num = Integer.parseInt(id.toString().substring(4));
            num++;
            if (num < 10) {
                return "AOID00" + num;
            } else if (num < 100) {
                return "AOID0" + num;
            } else {
                return "AOID" + num;
            }
        }
    }

