    private void addtextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addtextActionPerformed
        int row = stockTable.getSelectedRow();
        String id = (String) stockTable.getModel().getValueAt(row, 0);
        String qty = addtext.getText();
        String unitPrice = (String) stockTable.getModel().getValueAt(row, 3);
        int tot;
        tot = Integer.parseInt(qty) * Integer.parseInt(unitPrice);
        String totalPrice = tot + "";
        DefaultTableModel dtm = (DefaultTableModel) orderDetailLsitTable.getModel();

        String qtyOnHand = (String) stockTable.getValueAt(row, 2).toString();
        boolean b=true;
        for (int i = 0; i < orderDetailLsitTable.getRowCount(); i++) {
            if(id.equals(orderDetailLsitTable.getModel().getValueAt(i, 0).toString())){
                JOptionPane.showMessageDialog(agencyCombo, "Item Already Exists");
                b=false;
            }
        }
        if (b == true) {
            if (Integer.parseInt(qty) < Integer.parseInt(qtyOnHand)) {
                Object[] rows = {id, qty, unitPrice, totalPrice};
                dtm.addRow(rows);
                stockTable.setValueAt(Integer.parseInt(qtyOnHand) - Integer.parseInt(qty), row, 2);

            } else {
                System.err.println("Invalid qty");
                JOptionPane.showMessageDialog(this, "Invalid Qty");
            }
        }
        //get the sub total....
        //totText.setText("0");
        double total1=Double.parseDouble(tot+"");
        double subTotal=Double.parseDouble(totText.getText())+total1;
        totText.setText(subTotal+"");
        
        saveButton.setEnabled(true);
        printButton.setEnabled(true);

    }//GEN-LAST:event_addtextActionPerformed

