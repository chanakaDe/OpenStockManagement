    private String generateID() throws ClassNotFoundException, SQLException {
        String EmpID = EmployeeController.getLastId();
        if (EmpID == null) {
            return "E0001";
        } else {
            int num = Integer.parseInt(EmpID.substring(1));
            num++;
            if (num < 10) {
                return "E000" + num;
            } else if (num < 100) {
                return "E00" + num;
            } else if (num < 1000) {
                return "E0" + num;
            } else {
                return "E" + num;
            }
        }
    }

