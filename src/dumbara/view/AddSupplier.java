    public String generateID() throws ClassNotFoundException, SQLException{
        String id=SupplierController.getLastId();
        if(id==null){
            return "S001";
        }else{
            int num=Integer.parseInt(id.substring(1));
            num++;
            if(num<10){
                return "S00"+num;
            }else if(num<100){
                return "S0"+num;
            }else{
                return "S"+num;
            }
        }
    }

