    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String id=idText.getText();
        String name=nameText.getText();
        String address=addressText.getText();
        String conNo=contactNoText.getText();
        
        Supplier supplier=new Supplier(id, name, address, conNo);
        try {
            int res=SupplierController.addSupplier(supplier);
            if(res>0){
                JOptionPane.showMessageDialog(this, "Added "+id);
            }else{
                JOptionPane.showMessageDialog(this, "Error Adding "+id);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

