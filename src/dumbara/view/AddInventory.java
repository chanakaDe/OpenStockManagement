    private String generateID() throws ClassNotFoundException, SQLException{
        String ItemCode=InventoryController.getLastID();
        if(ItemCode==null){
            return "IT0001";
        }else{
            int num=Integer.parseInt(ItemCode.substring(2));
            num++;
            if(num<10){
                return "IT000"+num;
            }else if(num<100){
                return "IT00"+num;
            }else if(num<1000){
                return "IT0"+num;
            }else if(num<10000){
                return "IT"+num;
            }else{
                return null;
            }
        }
    }

