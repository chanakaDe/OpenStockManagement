    private void itemComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemComboActionPerformed
        String id=itemCombo.getSelectedItem().toString().split("-")[0];
        try {
            Inventory[] inventorys=InventoryController.viewInventorybyID(id);
            for (Inventory inventory : inventorys) {
                descText2.setText(inventory.getDesc());
                brandText2.setText(inventory.getBrandName());
                priceText2.setText(inventory.getPrice());
                purchasedDateText.setText(inventory.getPucharsedDate());
                warrantyPeriodText2.setText(inventory.getWarrantyPeriod());
                warrantyEndDate.setText(inventory.getWarrantyEndDate());
                nameText1.setText(inventory.getSupplierName());
                addressText1.setText(inventory.getSupplierAddress());
                contactNoText1.setText(inventory.getSupplierContactNo());
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddInventory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_itemComboActionPerformed

