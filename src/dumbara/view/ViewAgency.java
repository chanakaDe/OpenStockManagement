    private void updateButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateButtonMouseClicked

        int row = agencyTable.getSelectedRow();
        if (agencyTable.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        } else {
            int x = evt.getXOnScreen();
            int y = evt.getYOnScreen();

            String id = (String) agencyTable.getModel().getValueAt(row, 0);
            String name = (String) agencyTable.getModel().getValueAt(row, 1);
            String address = (String) agencyTable.getModel().getValueAt(row, 2);
            String contactNo = (String) agencyTable.getModel().getValueAt(row, 3);
            String ownerName = (String) agencyTable.getModel().getValueAt(row, 4);
            String ownerContactNo = (String) agencyTable.getModel().getValueAt(row, 5);

            Agency agency = new Agency(id, name, address, contactNo, ownerName, ownerContactNo);
            new UpdateAgency(this, true, x - 100, y - 100, agency).setVisible(true);
        }


    }//GEN-LAST:event_updateButtonMouseClicked

