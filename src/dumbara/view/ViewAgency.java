    private String generateID() throws ClassNotFoundException, SQLException {
        String agencyID = AgencyController.getLastId();
        if (agencyID == null) {
            return "A0001";
        } else {
            int num = Integer.parseInt(agencyID.substring(1));
            num++;
            if (num < 10) {
                return "A000" + num;
            } else if (num < 100) {
                return "A00" + num;
            } else if (num < 1000) {
                return "A0" + num;
            } else if (num < 10000) {
                return "A" + num;
            } else {
                return null;
            }
        }
    }

