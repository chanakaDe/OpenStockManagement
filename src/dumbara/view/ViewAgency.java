    private void ownerTelTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ownerTelTextKeyReleased
        // TODO add your handling code here:

        String txt = ownerTelText.getText();
        int caretPosition = ownerTelText.getCaretPosition();
        if (!txt.matches("^[0-9]{0,10}$")) {
            ownerTelText.setText(txt.substring(0, caretPosition - 1) + txt.substring(caretPosition));
            ownerTelText.setCaretPosition(caretPosition - 1);
        }

        if (!idText1.getText().equals("") && !nameText1.getText().equals("") && !addressText.getText().equals("") && !contactText.getText().equals("") && !ownerNameText.getText().equals("") && !ownerTelText.getText().equals("")) {
            addButton.setEnabled(true);
        }
    }//GEN-LAST:event_ownerTelTextKeyReleased

