    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String AgencyID = idText1.getText();
        String AgencyName = nameText1.getText();
        String address = addressText.getText();
        String conNo = contactText.getText();
        String ownerName = ownerNameText.getText();
        String ownerTelNo = ownerTelText.getText();

        Agency agency = new Agency(AgencyID, AgencyName, address, conNo, ownerName, ownerTelNo);
        try {
            int res = AgencyController.addAgency(agency);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Added ");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewAgency.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            Agency[] agencys = AgencyController.viewAllAgency();
            DefaultTableModel dtm = (DefaultTableModel) agencyTable.getModel();
            dtm.setRowCount(0);
            System.out.println(agencys.length);
            for (Agency agency1 : agencys) {
                Object[] row = {agency1.getAgencyID(), agency1.getAgencyName(), agency1.getAddress(), agency1.getContactNo(), agency1.getOwnerName(), agency1.getOwnerNo()};
                dtm.addRow(row);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewAgency.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            idText1.setText(generateID());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        nameText1.setText("");
        addressText.setText("");
        contactText.setText("");
        ownerNameText.setText("");
        ownerTelText.setText("");
        camcelButton.requestFocus();
        /////////////////////////////////////
    }//GEN-LAST:event_addButtonActionPerformed

