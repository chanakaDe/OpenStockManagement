    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        String userName = userNameText.getText();

        try {
            toEmail = UserController.getEmail(userName);
            email = emailController.getEmailDetails();
            fromEmail = email.getEmail();
            password = email.getPassword();
            emailUserName = fromEmail.split("@")[0];
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(NewLoginDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

        Properties props = new Properties();
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.socketFactory.port", "465");
        props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.port", "465");

        Session session = Session.getDefaultInstance(props,
                new javax.mail.Authenticator() {
                    @Override
                    protected PasswordAuthentication getPasswordAuthentication() {
                        return new PasswordAuthentication(emailUserName, password);
                    }
                });

        try {

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(fromEmail));
            message.setRecipients(Message.RecipientType.TO,
                    InternetAddress.parse(toEmail));
            message.setSubject("Reset Password");

            for (int i = 0; i < 5; i++) {
                n += (char) (r.nextInt(53) + 47);
            }

            message.setText("Dear " + userName + " , \n\n Your temporary password is : " + n + "\n Please change this password within 1 day. ");

            Transport.send(message);

            System.out.println("Done");

            try {
                int updateUser = UserController.updateUser(n, userName);
                if (updateUser > 0) {
                    System.out.println("updated pwd...");
                } else {
                    System.out.println("Error pwd...");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(ChangeUserDetails.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (MessagingException e) {
            throw new RuntimeException(e);
        }
    }//GEN-LAST:event_jLabel9MouseClicked

