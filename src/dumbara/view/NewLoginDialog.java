    private void userNameTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_userNameTextKeyReleased
        try {
            //userNameText.setBackground(Color.red);
            User[] users = UserController.viewUsers();
            for (User user : users) {
                if (userNameText.getText().equals(user.getUserNAme())) {
                    userNameText.setBackground(Color.green);

                    forgotPwdText.setVisible(true);
                    jLabel9.setVisible(true);
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(NewLoginDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (!userNameText.getText().equals("")) {
            forgotPwdText.setEnabled(true);
            jLabel9.setEnabled(true);

        }
    }//GEN-LAST:event_userNameTextKeyReleased

