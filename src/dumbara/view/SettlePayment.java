    private String fillLoanSettleID() {
        try {
            String lsID = LoanSettleController.getLastID();
            if (lsID == null) {
                return "LS0001";
            } else {
                int num = Integer.parseInt(lsID.substring(2));
                num++;
                if (num < 10) {
                    return "LS000" + num;
                } else if (num < 100) {
                    return "LS00" + num;
                } else if (num < 1000) {
                    return "LS0" + num;
                } else {
                    return "LS" + num;
                }

            }

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(SettlePayment.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
