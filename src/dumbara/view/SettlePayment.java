    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        double duPayment = (Double.parseDouble(duePaymentText.getText())) - (Double.parseDouble(paymentText.getText()));
        duePaymentText.setText(duPayment + "");
        duePaymentText.requestFocus();
        String loanID = loan.getLoanID();
        String loanAmount = loanAmountText.getText();
        String payment = paymentText.getText();
        String duePayment = duePaymentText.getText();
        String loanSettleID = LoanSettleIDText.getText();

        LoanSettle loanSettle = new LoanSettle(loanSettleID, loanID, loanAmount, payment, duePayment);
        try {
            int res = LoanSettleController.addLoanSettle(loanSettle);

            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Added");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(SettlePayment.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_updateButtonActionPerformed

