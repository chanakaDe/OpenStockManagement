    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String id = idText.getText();
        Date date = saleDatePicker.getDate();
        String agencyID = agencyIDCombo.getSelectedItem().toString().split("-")[0];
        String bottleID = bottleIDCombo.getSelectedItem().toString().split("-")[0];
        String bottleVol = bottleIDCombo.getSelectedItem().toString().split("-")[1];
        int qty = Integer.parseInt(qtyText.getText());
        double income = Double.parseDouble(incomeText.getText());


        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String format = dateFormat.format(date);

        System.out.println(id + " " + agencyID + " " + bottleID + " " + bottleVol + " " + qty + " " + income + " " + format);

        Sale sale = new Sale(id, format, agencyID, bottleID, bottleVol, qty, income);
        try {
            int res = SalesController.addSales(sale);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Added ");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }
        refresh();
        resetAll();        
    }

    public void refresh() {
        try {
            Sale[] sales = SalesController.viewAllSales();
            DefaultTableModel dtm = (DefaultTableModel) salesTable.getModel();
            dtm.setRowCount(0);
            for (Sale sale1 : sales) {
                Object[] rows = {sale1.getSalesID(), sale1.getSalesDate(), sale1.getAgencyID(), sale1.getBottleID(), sale1.getSaledQty(), sale1.getIncome()};
                dtm.addRow(rows);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_submitButtonActionPerformed

