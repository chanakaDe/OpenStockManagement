    public String generateID() throws ClassNotFoundException, SQLException {
        String id = SalesController.getLastID();
        if (id == null) {
            return "SID0001";
        } else {
            int num = Integer.parseInt(id.toString().substring(3));
            num++;
            if (num < 10) {
                return "SID000" + num;
            } else if (num < 100) {
                return "SID00" + num;
            } else if (num < 1000) {
                return "SID0" + num;
            } else if (num < 10000) {
                return "SID" + num;
            } else {
                return null;
            }
        }
    }

