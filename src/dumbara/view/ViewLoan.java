    public void refresh(){
        try {
            Loan[] loan=LoanController.viewLoan();
            DefaultTableModel dtm=(DefaultTableModel) loanTable.getModel();
            dtm.setRowCount(0);
            for (Loan loan1 : loan) {
                Object[] rows={loan1.getLoanID(),loan1.getEmpID(),loan1.getLongAmount(),loan1.getLoanTerm(),loan1.getInterestRate(),loan1.getMonthlyPayment(),loan1.getDueAmount()};
                dtm.addRow(rows);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewLoan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void settleButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_settleButtonMouseClicked
        x=evt.getXOnScreen();
        y=evt.getYOnScreen();
        
        int row=loanTable.getSelectedRow();
        String loanAmount=(String) loanTable.getModel().getValueAt(row, 2);
        String monthlyPayment=(String) loanTable.getModel().getValueAt(row, 5);
        String loanID=(String) loanTable.getValueAt(row, 0);
        
        Loan loan=new Loan(loanAmount,monthlyPayment,loanID);
        
        new SettlePayment(this, true,x-100,y-100,loan).setVisible(true);
    }//GEN-LAST:event_settleButtonMouseClicked

    private void removeLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeLoanActionPerformed
        int row=loanTable.getSelectedRow();
        String LoanID=(String) loanTable.getModel().getValueAt(row, 0);
        try {
            int res=LoanController.deleteLoan(LoanID);
            if(res>0){
                JOptionPane.showMessageDialog(this, "Deleted");
                refresh();
            }else{
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewLoan.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_removeLoanActionPerformed

