    private void loanIdTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_loanIdTextKeyReleased
        String loanId=loanIdText.getText();
        DefaultTableModel dtm=(DefaultTableModel) loanTable.getModel();
        dtm.setRowCount(0);
        for(Object[] row:rowList){
            if(row[0].toString().contains(loanId)){
                dtm.addRow(row);
            }
        }
    }//GEN-LAST:event_loanIdTextKeyReleased

