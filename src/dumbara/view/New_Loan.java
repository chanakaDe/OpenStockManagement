    private void loanTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loanTableMouseClicked
        //get suitable loan settle details in the table
        int row = loanTable.getSelectedRow();
        String loanID = loanTable.getModel().getValueAt(row, 0).toString();

        try {
            LoanSettle[] ls = LoanSettleController.viewLoanSettlebyEmp(loanID);
            DefaultTableModel dtm = (DefaultTableModel) loanHistoryTable.getModel();
            dtm.setRowCount(0);
            for (LoanSettle loanSettle : ls) {
                Object[] rows = {loanSettle.getLoanSettleID(), loanSettle.getLoanID(), loanSettle.getPayment(), loanSettle.getLoanAmount(), loanSettle.getDuePayment(), loanSettle.getPaymentDate()};
                dtm.addRow(rows);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(PaymentHistory.class.getName()).log(Level.SEVERE, null, ex);
        }
        //get the sum of all the settlements
        double sum=0;
        for (int i = 0; i < loanHistoryTable.getRowCount(); i++) {
            sum+=Double.parseDouble(loanHistoryTable.getValueAt(i, 2).toString());
        }
        jTextField1.setText(sum+"");
        // get on hand salary
        double onHand=Double.parseDouble(salaryTextA.getText())-sum;
        civilStatusTextA2.setText(onHand+"");
    }//GEN-LAST:event_loanTableMouseClicked

