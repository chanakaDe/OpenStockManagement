    private String generateID() throws ClassNotFoundException, SQLException{
        String loanID=LoanController.getLastID();
        if(loanID==null){
            return "LN0001";
        }else{
            int num=Integer.parseInt(loanID.substring(2));
            num++;
            if(num<10){
                return "LN000"+num;
            }else if(num<100){
                return "LN00"+num;
            }else if(num<1000){
                return "LN0"+num;
            }else{
                return "LN"+num;
            }
        }
    }


