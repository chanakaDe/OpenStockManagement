    private void executeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_executeButtonActionPerformed
        // TODO add your handling code here:
        String name=nameText.getText();
        String address=addressText.getText();
        String telNo=telNoText.getText();
        String nic=nicText.getText();
        
        Customer customer=new Customer(this.customer.getCustID(), name, address, telNo, nic);
        try {
            int res=CustomerController.updateCustomer(customer);
            dispose();
            if(res>0){
                JOptionPane.showMessageDialog(this,"Updated");
            }else{
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(UpdateCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_executeButtonActionPerformed

