    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email=jTextField1.getText();
        String pwd=jTextField2.getText();
        
        Email email1=new Email(email, pwd);
        try {
            int addEmail = emailController.addEmail(email1);
            if (addEmail>0) {
                JOptionPane.showMessageDialog(rootPane, "Added...");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Error...");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(EmailConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String email = jTextField1.getText();
        String pwd = jTextField2.getText();

        Email email1 = new Email(email, pwd);
        try {
            int addEmail = emailController.updateEmail(email1);
            if (addEmail > 0) {
                JOptionPane.showMessageDialog(rootPane, "Updated...");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error...");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(EmailConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
        //
        try {
            Email email11 = emailController.getEmailDetails();
            jTextField1.setText(email11.getEmail());
            jTextField2.setText(email11.getPassword());
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(EmailConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

